package com.dayuanit.ape.dto;

public class SchoolDTO {
	private String schoolName;
	private String graduTime;
	private String degree;
	private String eduDesc;

	public String getSchoolName() {
		return schoolName;
	}

	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}

	public String getGraduTime() {
		return graduTime;
	}

	public void setGraduTime(String graduTime) {
		this.graduTime = graduTime;
	}

	public String getDegree() {
		return degree;
	}

	public void setDegree(String degree) {
		this.degree = degree;
	}

	public String getEduDesc() {
		return eduDesc;
	}

	public void setEduDesc(String eduDesc) {
		this.eduDesc = eduDesc;
	}

}
